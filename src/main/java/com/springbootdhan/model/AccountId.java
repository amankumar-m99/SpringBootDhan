package com.springbootdhan.model;

public class AccountId {
	private String accountId;

	public AccountId() {
		super();
	}

	public AccountId(String accountId) {
		super();
		this.accountId = accountId;
	}

	public String getAccountId() {
		return accountId;
	}

	public void setAccountId(String id) {
		this.accountId = id;
	}
}
